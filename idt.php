                        <h2>Isolation de la<em> toiture</em></h2>
                        <p>Quel que soit son type, la toiture protège l’ensemble de l’édifice contre les effets du mauvais temps et du soleil. Héritage des traditions régionales ou de l’évolution des techniques de construction, la toiture revêt des formes multiples et une grande diversité de matériaux. Dans tous les cas, elle ne doit jamais être négligée. Son intégrité garantit en effet la pérennité du bâtiment qu’elle protège. Quelques explications pour choisir une toiture qui saura durer dans le temps face aux rigueurs du climat.</p>
                                <br>
                        <h2>La composition de la toiture</h2>
                        <p>Installée en partie supérieure de l’habitation, en appui sur ses murs porteurs, la toiture se compose d’une charpente et d’une couverture. L’espace intérieur situé entre ces deux composantes, appelé combles, peut parfois être aménageable. Ces combles accueillent l'isolation sous-toit. La charpente, qui correspond à un assemblage de poutres et de linteaux de bois et plus rarement de barres métalliques, supporte une couverture dont la nature varie grandement d’une zone géographique à l’autre. L’ensemble est étudié pour favoriser l’écoulement des eaux de pluies et résister au poids de la neige ou aux effets du vent. Une bonne toiture se doit d’être étanche et esthétique.</p>
                                <br>
                        <h2>Quelle toiture choisir ?</h2>
                        <p>Vous devez en tout premier lieu vous renseigner sur les règles d’urbanisme en vigueur dans votre commune. La toiture est un élément particulièrement visible et constitutif de l’identité architecturale régionale. Ces règles figurent au DTU (Document Technique Unifié) et dans les règlements des zones du Plan Local d’urbanisme consultables en mairie au service urbanisme. De nombreux matériaux peuvent en effet être utilisés pour la couverture : tuile béton, tuile romaine, ardoise, bardeau de bois, acier, cuivre, tôle ondulée, matière plastique ou shingle.</p>

                        <p>Ils ne sont pas forcément tous autorisés dans votre région, n'hésitez pas à contacter votre mairie pour vous renseigner. Par la suite les critères à prendre en compte sont :</p>
                    <ul class="ul-class">
                        <li>le climat pour la résistance de la charpente et l’isolation</li>
                        <li>la résistance au feu</li>
                        <li>la durabilité</li>
                        <li>l’esthétique</li>
                        <li>le prix pour rentrer dans l’enveloppe budgétaire</li>
                    </ul>

                                <br>
                        <h2>L’installation de la toiture</h2>
                        <p>Comme tous les corps de métiers du bâtiment, les charpentiers et les couvreurs possèdent le savoir-requis pour respecter les règles de l’Art et offrir une garantie décennale rassurante pour le donneur d'ordre. Faire appel à un professionnel de sa région c’est l’assurance d’une maîtrise des techniques et usages traditionnels observés dans cette dernière. Le prix est quant à lui très variable. Il dépend directement du choix des matériaux et notamment de celui de la couverture. A titre indicatif le m² de tuiles bétons se situe autour de 27€ tandis que celui de l’ardoise naturelle atteint les 120€ !</p>
                                <br>
                        <h2>L'entretien</h2>
                        <p>Le temps fait son œuvre, il est donc important d’entretenir régulièrement sa toiture. Les mousses et lichens qui s’installent sur les tuiles retiennent l’humidité. Leur prolifération peut donc nuire à l’étanchéité de la couverture tout comme à son esthétique. Il est recommandé de procéder à un petit nettoyage annuel en pulvérisant à basse pression un produit anti-mousse (démoussage). Le nettoyage haute-pression est à proscrire absolument. Vous risquez de détériorer votre couverture et de compromettre son étanchéité. Cet entretien régulier vous obligera à monter sur le toit, vous pourrez alors profiter de l'occasion pour vérifier l’état de la couverture. Une tuile cassée ou un bardeau arraché doit immédiatement être changé. Tous les 3 ou 4 ans vous pouvez aussi procéder à un traitement hydrofuge pour limiter les risques d’infiltration d’eau et revitaliser votre couverture. Une charpente bois se traite quant à elle tous les 10 ans contre les insectes xylophages. Ces entretiens réguliers vont considérablement prolonger la durée de vie de votre toiture. Ces petites dépenses seront largement compensées par cet allongement. Il serait en effet dommage de devoir investir prématurément dans une nouvelle toiture par défaut d’entretien.</p>
                                <br>
