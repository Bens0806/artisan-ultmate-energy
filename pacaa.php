                        <h2>Tout savoir sur <em>la pompe à chaleur air-air</em></h2>
                        <p>La pompe à chaleur air-air utilise les calories de l’air, une énergie gratuite, pour chauffer efficacement et à moindre coût votre logement. Elle permet, en effet, de diviser par 3 votre facture de chauffage. Ces économies font de la pompe à chaleur air-air un système rentable en seulement quelques années. Le saviez-vous ? Vous pouvez installer votre pompe à chaleur air-eau (qui chauffe votre logement mais aussi votre eau chaude sanitaire) à partir de 1500€ seulement !</p>
                                <br>
                        <h2>Comment fonctionne une PAC air-air ?</h2>
                        <p>Le principe de fonctionnement de la pompe à chaleur air-air est simple. L’appareil capte les calories présentes dans l’air extérieur pour chauffer un logement. Pour fonctionner, la pompe à chaleur a besoin de deux unités. L’une, située à l’extérieur, récupère les calories de l’air et l’autre, à l’intérieur, la diffuse au sein du logement grâce à un ventilo-convecteur.</p>
                                <br>
                        <h2>Vos factures de chauffage divisées par 3 !</h2>
                        <p>En utilisant l’énergie gratuite de l’air, vous ne payez que l'électricité nécessaire à la circulation du fluide caloporteur pour faire fonctionner la pompe à chaleur. Vous pouvez ainsi diviser vos factures de chauffage par 3. Ces économies permettent d’amortir en seulement quelques années le prix de la pompe à chaleur air-air qui varie entre 1.600 € et 2.800 € par unité intérieure installée.</p>
                                <br>
                        <h2>Quel entretien pour votre PAC air-air ?</h2>
                        <p>Si votre appareil contient plus de 2 kilogrammes de fluide frigorigène, alors un entretien est obligatoire une fois par an. Celui-ci doit être effectué par un installateur qualifié et peut être prévu dans un contrat d'entretien souscrit lors de l'installation, ce qui est vivement recommandé. De plus, un entretien minime et régulier, tel qu’un dépoussiérage de l’unité extérieure, est nécessaire. Cela permet d’allonger la durée de vie de l’appareil, qui peut aller jusqu’à 20 ans.</p>
                                <br>
                        <h2>Puis-je installer une PAC air-air chez moi ?</h2>
                        <p>L’installation d’une pompe à chaleur air-air est très simple. En effet, il suffit de disposer d'un jardin ou d’une cour pour y installer l'unité extérieure. Un conseil : choisissez un emplacement qui occasionne le moins de bruit pour le voisinage (distance minimale, éviter les angles et recoins qui renvoient le son). Il vous faut aussi un emplacement dans votre logement pour installer les unités intérieures. Les ventilo-convecteurs s'installent simplement au-dessus des portes des pièces à chauffer. La jonction entre les deux est assurée par un circuit de fluide frigorigène.</p>
                                <br>
                        <h2>Qui de la PAC réversible ? </h2>
                        <p>En inversant le mode de fonctionnement de votre pompe à chaleur air-air , elle peut aussi produire de la fraîcheur, et fonctionner comme une climatisation pour votre logement. Cette double fonctionnalité, ajoutée au faible coût de l’appareil, en font une solution particulièrement intéressante.</p>
                                <br>
                        <h2>Quelle puissance pour votre PAC air-air ?</h2>
                        <p>Les principales caractéristiques à connaître pour juger de la performance d'une pompe à chaleur air-air sont :</p>
                                <br>
                    <ul class="ul-class">     
                        <li>Le coefficient de performance (COP) qui représente le nombre de kWh de chaleur produits, pour 1 kWh d'électricité consommée. Le COP de la pompe à chaleur air-air se situe généralement autour de 3. La consommation électrique de la pompe à chaleur est donc minime.</li>
                        <li>La puissance calorifique (en kW) qui représente la capacité de production de chaleur de la PAC. Les pompes à chaleur air-air actuelles ont une puissance comprise entre 5 et 20 kW.</li>       
