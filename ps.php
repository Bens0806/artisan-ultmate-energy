                        <p>Pour installer des panneaux solaires photovoltaïques, il n'est pas obligatoire d'habiter dans le Sud de la France. L’idéal est de disposer d’une pente de toit bien ensoleillée avec une inclinaison de 30°, plutôt orientée au Sud. Le photovoltaïque est donc une solution envisageable partout en France. </p>
