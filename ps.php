                            <p>Il est possible de revendre son électricité solaire à un tarif de rachat très avantageux, de 0,1873 € par kWh. Ce prix est supérieur à celui auquel nous achetons l'électricité. Il est donc intéressant de revendre l'intégralité de l'électricité au réseau, ce qui génère des revenus très intéressants, jusqu'à 1.800 € /an pour une installation de 6 kWc. De cette façon, vos panneaux solaires photovoltaïques peuvent vous permettre de générer d’importants revenus pendant 20 ans.<p>
