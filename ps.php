                        <h2>Comment ça marche ? </h2>
                            <p>Grâce à des matériaux dits "semi-conducteurs", les panneaux photovoltaïques produisent de l'électricité lorsqu'ils sont éclairés par le soleil. Les cellules qui les composent transforment l’énergie solaire en un courant continu. Celui-ci est transformé en courant alternatif par un onduleur pour la revente au réseau ou une autoconsommation.</p>
                                <br>
