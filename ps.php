                                <br>
                        <h2>La puissance des panneaux solaires</h2>
                        <p>La principale caractéristique technique à connaître pour juger de la performance d'un panneau solaire photovoltaïque est sa puissance (en kW). Celle-ci dépend du matériau et de la surface des panneaux. La puissance de production, elle, détermine la capacité qu’ont les panneaux solaires à produire de l’électricité dans des conditions idéales (inclinaison, ensoleillement, orientation...).</p>
                                <br>
